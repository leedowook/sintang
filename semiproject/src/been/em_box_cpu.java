package been;

public class em_box_cpu {
	private int num;
	private int cpu_num;
	private int count;
	private int price;
	public int getNum() {
		return num;
	}
	public int getCpu_num() {
		return cpu_num;
	}
	public int getCount() {
		return count;
	}
	public int getPrice() {
		return price;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public void setCpu_num(int cpu_num) {
		this.cpu_num = cpu_num;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
	
}
