package bunsu516;

public class pratice {
	public static void main(String[] args) {
		int x=1;
		int y=-1;
		
		
		System.out.println("x++="+ x++);
		System.out.println("++x="+ ++x);
		System.out.println("--x="+--x);
		System.out.println("x--="+x--);
		System.out.println("++y="+ ++y);
		System.out.println("y++="+y++);
		System.out.println("y--="+y--);
		System.out.println("--y="+--y);
	}
}
